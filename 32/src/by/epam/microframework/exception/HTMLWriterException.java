
public class HTMLWriterException extends Exception {

	public HTMLWriterException() {
		super();
	}

	public HTMLWriterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HTMLWriterException(String message, Throwable cause) {
		super(message, cause);
	}

	public HTMLWriterException(String message) {
		super(message);
	}

	public HTMLWriterException(Throwable cause) {
		super(cause);
	}

}
