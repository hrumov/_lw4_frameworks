
public class HTMLAnalyzerException extends Exception {

	public HTMLAnalyzerException() {
		super();
	}

	public HTMLAnalyzerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HTMLAnalyzerException(String message, Throwable cause) {
		super(message, cause);
	}

	public HTMLAnalyzerException(String message) {
		super(message);
	}

	public HTMLAnalyzerException(Throwable cause) {
		super(cause);
	}

}
