
public class MalformedCommandFileException extends RuntimeException {

	public MalformedCommandFileException() {
		super();
	}

	public MalformedCommandFileException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MalformedCommandFileException(String message, Throwable cause) {
		super(message, cause);
	}

	public MalformedCommandFileException(String message) {
		super(message);
	}

	public MalformedCommandFileException(Throwable cause) {
		super(cause);
	}
	
	

}
