
public class CommandReaderException extends Exception {

	public CommandReaderException() {
		super();
	}

	public CommandReaderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CommandReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommandReaderException(String message) {
		super(message);
	}

	public CommandReaderException(Throwable cause) {
		super(cause);
	}
	
	

}
