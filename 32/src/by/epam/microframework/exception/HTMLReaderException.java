
public class HTMLReaderException extends Exception {

	public HTMLReaderException() {
		super();
	}

	public HTMLReaderException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HTMLReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public HTMLReaderException(String message) {
		super(message);
	}

	public HTMLReaderException(Throwable cause) {
		super(cause);
	}

}
